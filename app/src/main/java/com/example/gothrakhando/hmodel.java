package com.example.gothrakhando;

public class hmodel {

    public int himage;
    public String hname;
    public String hgothram;

    public hmodel(int himage, String hname, String hgothram) {
        this.himage = himage;
        this.hname = hname;
        this.hgothram = hgothram;
    }

    public int getHimage() {
        return himage;
    }

    public String getHname() {
        return hname;
    }

    public String getHgothram() {
        return hgothram;
    }
}
